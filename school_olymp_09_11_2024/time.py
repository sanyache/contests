"""
Задано деякий час (g – години, h – хвилини, s – секунди). Визначити, який час буде
через 1 секунду після заданого
15 30 25     15 30 26
10 20 59     10 21 0
"""
g, h, s = map(int, input().split())
all_sec = g*3600 + h*60 + s + 1
g = (all_sec//3600)%24
h = all_sec%3600//60
s = all_sec%3600%60
print(g, h, s)
